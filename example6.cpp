#include <iostream>
#include <cstdlib>  // For rand() and srand()
#include <ctime>    // For time()

// Definition of the Point class
class Point {
private:
    int x;
    int y;

public:
    // Constructor to initialize x and y to 0
    Point() : x(0), y(0) {}

    // Setters for x and y
    void SetX(int xVal) { x = xVal; }
    void SetY(int yVal) { y = yVal; }

    // Getters for x and y
    int GetX() const { return x; }
    int GetY() const { return y; }

    // Print Point coordinates
    void Print() const {
        std::cout << "(" << x << ", " << y << ")";
    }
};

// Function to create n random points
Point* createNRandPoints(int n) {
    const int MAX = 100;  // The maximum value for coordinates

    // Dynamically allocate memory for n points
    Point *points = new Point[n];

    // Seed the random number generator
    srand(static_cast<unsigned>(time(0)));

    // Generate random points
    for (int i = 0; i < n; i++) {
        points[i].SetX(rand() % MAX);
        points[i].SetY(rand() % MAX);
    }

    return points;  // Return the dynamically allocated array of points
}

// Main function to test the createNRandPoints function
int main() {
    int n;  // Number of points to create
    std::cout << "Enter the number of random points to generate: ";
    std::cin >> n;

    // Create n random points
    Point *points = createNRandPoints(n);

    // Display the generated points
    std::cout << "Generated Points:" << std::endl;
    for (int i = 0; i < n; i++) {
        points[i].Print();
        std::cout << std::endl;
    }

    // Free dynamically allocated memory
    delete[] points;

    return 0;
}
